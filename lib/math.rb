def addition(num1, num2)
  sum = num1 + num2
  p sum
end

def subtraction(num1, num2)
  dif = num1 - num2
  p dif
end

def division(num1, num2)
  quotient = num1 / num2
  p quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  p product
end

def modulo(num1, num2)
  quotient = num1 / num2
  r = quotient * num2
  remainder = num1 - r 
  p remainder
end

